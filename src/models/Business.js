class Business {
    constructor(image, name, address, city, state, zipcode, category, rating, reviewCount) {
        this.image = image;
        this.name = name;
        this.address = address;
        this.city = city;
        this.state = state;
        this.zipcode = zipcode;
        this.category = category;
        this.rating = rating;
        this.reviewCount = reviewCount;
    }
}

export default Business;
